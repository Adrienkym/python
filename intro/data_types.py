#Advanced data types 
my_array= [10, True, "World", 3.14,[]] #list<array>
print(my_array) 
print(my_array[2]) #accessing element at index 2
print(type(my_array)) 

my_set = {1, 2, 3, 4, 5, 'hello'} #set(unordered collection of unique elements)
print(my_set) 
print(type(my_set))
my_tuple = (1, 2, 3,4.5, 'Apple') #tuple my_tuple[4] = 'Apple' (ordered collection of elements, immutable)
print(my_tuple)
print(my_tuple[3]) #accessing element at index 3
print(type(my_tuple)) 
my_dict = {'name': 'John', 'age': 30, 'is_student': False} #dictionary key-value pairs (object) e,g my_dict['name'] = 'John'
print(my_dict)
print(type(my_dict) )


